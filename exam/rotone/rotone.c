#include <unistd.h>

int	main(int ar, char *ac[])
{
	int	i;
	char 	l;

	if (ar == 2)
	{
		i = 0;
		while (ac[1][i] != '\0')
		{
			l = ac[1][i];
			if (ac[1][i] == 'z' || ac[1][i] == 'Z')
				l -= 25;
			else
				l++;
			write(1, &l, 1);
			i++;
		}
	}
	write(1, "\n", 1);
	return (0);
}
