#include <unistd.h>

int	main(int c, char *p[])
{
	int	i;

	if (c == 4 && p[2][1] == '\0' && p[3][1] == '\0')
	{
		i = 0;
		while (p[1][i] != '\0')
		{
			if (p[1][i] == p[2][0])
					write(1, &p[3][0], 1);
			else
				write(1, &p[1][i], 1);
			i++;
		}
	}
	write(1, "\n", 1);
	return (0);
}
