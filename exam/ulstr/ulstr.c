#include <unistd.h>

int	main(int c, char *p[])
{
	int		i;
	char	l;

	if (c == 2)
	{
		i = 0;
		while (p[1][i] != '\0')
		{
			l = p[1][i];
			if (l >= 'a' && l <= 'z')
				l -= 32;
			else if (l >= 'A' && l <= 'Z')
				l += 32;
			write(1, &l, 1);
			i++;
		}
	}
	write(1, "\n", 1);
	return (0);
}
