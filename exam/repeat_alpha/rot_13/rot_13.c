#include <unistd.h>

int	main(int ar, char *ac[])
{
	int		i;
	char	l;

	if (ar == 2)
	{
		i = 0;
		while (ac[1][i] != '\0')
		{
			l = ac[1][i];
			if ((l >= 'a' && l <= 'z') || (l >= 'A' && l <= 'Z'))
			{
				if (l <= 'm' || l <= 'M')
					l += 13;
				else
					l -= 13;
			}
			write(1, &l, 1);
			i++;
		}
	}
	write(1, "\n", 1);
	return (0);
}
