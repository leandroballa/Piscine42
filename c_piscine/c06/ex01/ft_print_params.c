#include <unistd.h>

int	main(int argc, char *argv[])
{
	int	x;
	int	y;

	if (argc >= 2)
	{
		x = 1;
		while (x < argc)
		{
			y = 0;
			while (argv[x][y] != '\0')
			{
				write(1, &argv[x][y], 1);
				y++;
			}
			x++;
			write(1, "\n", 1);
		}
	}
	return (0);
}
